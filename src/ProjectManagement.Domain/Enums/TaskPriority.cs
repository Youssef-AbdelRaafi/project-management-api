/// </summary>
public enum TaskPriority
{
    /// <summary>
    /// Low priority work.
    /// </summary>
    Low = 1,

    /// <summary>
    /// Normal priority work.
    /// </summary>
    Medium = 2,

    /// <summary>
    /// High priority work.
    /// </summary>
    High = 3,

    /// <summary>
    /// Critical priority work.
    /// </summary>
    Critical = 4
}
